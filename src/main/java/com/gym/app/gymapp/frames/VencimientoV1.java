    private void cargarPagos() {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"Nombre", "Apellido", "Membres√≠a", "Fecha de Pago", "Vencimiento"});

        try (Connection con = ConexionBD.conectar(); Statement st = con.createStatement(); ResultSet rs = st.executeQuery(
                "SELECT c.Name, c.LastName, m.Name_Membership, p.Pay_Date, "
                + "DATE_ADD(p.Pay_Date, INTERVAL "
                + "CAST(REPLACE(m.Duration_Membership, ' dias', '') AS SIGNED) DAY) AS Expiration_Date "
                + "FROM payment p "
                + "JOIN client c ON p.Id_Client = c.Id_Client "
                + "JOIN memberships m ON p.Name_Membership = m.Name_Membership")) {

            while (rs.next()) {
                String nombre = rs.getString("Name");
                String apellido = rs.getString("LastName");
                String membresia = rs.getString("Name_Membership");
                Date fechaPago = rs.getDate("Pay_Date");
                Date vencimiento = rs.getDate("Expiration_Date");

                model.addRow(new Object[]{nombre, apellido, membresia, fechaPago, vencimiento});
            }

            tblVencimiento.setModel(model);

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error cargando pagos: " + ex.getMessage());
        }
    }


